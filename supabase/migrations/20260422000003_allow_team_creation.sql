-- ── 2. 팀 생성 권한(RLS) 추가 ──────────────────────────────────────
-- 기존: SELECT는 모두, UPDATE는 팀장만
-- 추가: INSERT는 인증된 사용자 누구나 가능
CREATE POLICY "인증된 사용자는 팀 생성 가능"
  ON teams FOR INSERT
  TO authenticated
  WITH CHECK (true);

-- profiles: 팀에 가입되지 않은 상태에서 본인의 team_id를 직접 수정할 수 있어야 함
-- 이미 policy "자신의 프로필만 수정"이 있으므로 profiles 테이블에 대한 UPDATE는 가능.
